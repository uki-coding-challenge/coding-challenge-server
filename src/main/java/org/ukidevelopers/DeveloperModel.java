package org.ukidevelopers;

public class DeveloperModel {

    private String username;
    private boolean tier0;
    private boolean tier1;
    private boolean tier2;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public boolean getTier0() {
        return tier0;
    }

    public void setTier0(boolean tier0) {
        this.tier0 = tier0;
    }

    public boolean getTier1() {
        return tier1;
    }

    public void setTier1(boolean tier1) {
        this.tier1 = tier1;
    }

    public boolean getTier2() {
        return tier2;
    }

    public void setTier2(boolean tier2) {
        this.tier2 = tier2;
    }
    
}
